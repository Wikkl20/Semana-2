    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        String strDocenas = txtCantidad.getText().trim();
        String strPrecio = txtPrecio.getText().trim();

        // Validar campos vacíos
        if (strDocenas.isEmpty() || strPrecio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese datos sin espacios vacios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar que sean números
        if (!strDocenas.matches("\\d+") || !strPrecio.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Ingrese valores numéricos válidos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int docenas = Integer.parseInt(strDocenas);
        double precio = Double.parseDouble(strPrecio);

        if (docenas <= 0 || precio <= 0) {
            JOptionPane.showMessageDialog(this, "Los valores deben ser mayores a 0", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Cálculos
        double importe = docenas * precio;
        double descuento = (docenas >= 3) ? importe * 0.15 : importe * 0.10;
        double total = importe - descuento;
        String obsequio = (docenas >= 3) ? (docenas - 3) * 2 + " lapiceros" : "Ninguno";

        DecimalFormat df = new DecimalFormat("S/ #,##0.00");

        lblImporte.setText(df.format(importe));
        lblDescuento.setText(df.format(descuento));
        lblTotalPagar.setText(df.format(total));
        lblObsequio.setText(obsequio);

    }//GEN-LAST:event_btnCalcularActionPerformed

